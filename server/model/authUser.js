import mongoose from "mongoose";

const authUserSchema = new mongoose.Schema({
  firstname: {
    type: String,
    required: true,
  },
  lastname: {
    type: String,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
});

const AuthUser = mongoose.model("AuthUser", authUserSchema);

export default AuthUser;